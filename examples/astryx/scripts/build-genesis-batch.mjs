/**
 * Astryx genesis — ONE paste-able script for the whole first library.
 *   `node examples/astryx/scripts/build-genesis-batch.mjs`
 *
 * Concatenates 00-tokens + the 13 component sync scripts in DEPENDENCY
 * ORDER, each wrapped in its own async block scope (per-script consts stay
 * isolated — the same isolation the plugin's per-paste runScript gives).
 * Paste the output ONCE into the plugin's "Paste a script" tab on a blank
 * file: tokens upsert first, then every component set, sections and all.
 * Deterministic end to end — emitted by the CLI from the contracts, executed
 * verbatim by the plugin, no AI in the conversion.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const EX = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const F = (f) => readFileSync(path.join(EX, 'figma', f), 'utf8');

// deps: toast+dropdown-menu need button/dropdown-menu-item synced first.
const ORDER = [
  '00-tokens.figma.js',
  'badge.figma.js', 'banner.figma.js', 'button.figma.js', 'card.figma.js',
  'checkbox-input.figma.js', 'progress-bar.figma.js', 'slider.figma.js',
  'switch.figma.js', 'text-input.figma.js', 'token.figma.js',
  'dropdown-menu-item.figma.js', 'toast.figma.js', 'dropdown-menu.figma.js',
];

const blocks = ORDER.map((f) => `// ───── ${f} ─────
await (async () => {
${F(f)}
})();`).join('\n\n');

const out = `// ASTRYX GENESIS — the first Astryx Figma library, one deterministic paste.
// GENERATED by examples/astryx/scripts/build-genesis-batch.mjs — do not edit.
// Order: tokens → primitives → composition set (dependencies first).
${blocks}
figma.notify('Astryx genesis complete: tokens + 13 component sets.');
`;
writeFileSync(path.join(EX, 'figma', 'GENESIS-BATCH.figma.js'), out);
console.log(`✔ GENESIS-BATCH.figma.js: ${ORDER.length} blocks, ${out.length} bytes`);
