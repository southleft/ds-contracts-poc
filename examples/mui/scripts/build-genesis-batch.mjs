/**
 * MUI genesis — ONE paste-able script for the whole first library.
 *   `node examples/mui/scripts/build-genesis-batch.mjs`
 *
 * Concatenates 00-tokens + the 5 component sync scripts, each wrapped in its
 * own async block scope (per-script consts stay isolated — the same isolation
 * the plugin's per-paste runScript gives). Paste the output ONCE into the
 * plugin's "Paste a script" tab on a blank file: tokens upsert first (912
 * variables, Light/Dark, 61 Figma-native aliases), then every component set.
 *
 * PROVEN before writing: the exact concatenated output is executed against
 * the mocked `figma` global (scripts/plugin-engine-mock-figma.mjs) and must
 * build every component set without throwing — the builder REFUSES to write
 * a batch it cannot run. Deterministic end to end — emitted by the CLI from
 * the contracts, executed verbatim by the plugin, no AI in the conversion.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';
import { createFigmaMock } from '../../../scripts/plugin-engine-mock-figma.mjs';

const EX = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const F = (f) => readFileSync(path.join(EX, 'figma', f), 'utf8');

const ORDER = [
  '00-tokens.figma.js',
  'button.figma.js', 'card.figma.js', 'chip.figma.js', 'slider.figma.js', 'switch.figma.js',
];

const blocks = ORDER.map((f) => `// ───── ${f} ─────
await (async () => {
${F(f)}
})();`).join('\n\n');

const out = `// MUI GENESIS — the first MUI (@mui/material@9.2.0 default theme) Figma library, one deterministic paste.
// GENERATED by examples/mui/scripts/build-genesis-batch.mjs — do not edit.
// Order: tokens (Light/Dark + Figma-native source aliases) → 5 component sets.
${blocks}
figma.notify('MUI genesis complete: tokens + 5 component sets.');
`;

// refuse-to-write proof: the EXACT output must run headlessly end to end
const mock = createFigmaMock();
const ctx = vm.createContext({ figma: mock.figma, console: { log() {}, warn() {}, error() {} } });
await vm.runInContext(`(async () => {\n${out}\n})()`, ctx, { timeout: 300_000 });
const sets = mock.root.findAll((n) => n.type === 'COMPONENT_SET');
const vars = await mock.figma.variables.getLocalVariablesAsync();
if (sets.length !== ORDER.length - 1) {
  console.error(`✘ genesis batch REFUSED: mock built ${sets.length}/${ORDER.length - 1} component sets (${sets.map((s) => s.name).join(', ')})`);
  process.exit(1);
}

writeFileSync(path.join(EX, 'figma', 'GENESIS-BATCH.figma.js'), out);
console.log(`✔ GENESIS-BATCH.figma.js: ${ORDER.length} blocks, ${out.length} bytes — mock-proven (${sets.length} sets: ${sets.map((s) => `${s.name}(${s.children.length})`).join(', ')}; ${vars.length} variables)`);
